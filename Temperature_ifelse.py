#what is the score
grade = int(input("what grade did you get? "))

# Determine the grade
if grade >= 90:
    print("Your grade is an A")

elif grade >= 80:
        print("Your grade is a B")

elif grade >=70:
        print("Your grade is a C")
    
elif grade >= 60:
        print("Your grade is a D")
    
else:
    print("Your grade is an F")